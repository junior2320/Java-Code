package class3919;

public class WhileAndDoWhile {

	public static void main(String[] args) {
		
		int a=10;
		
		do {//exetues first and then check condition
			System.out.println("Hello");
			a++;
	}while(a>15);
		  //--------------------------------------
		
		int b=10;
		while (b<15) {//check condition and then execute
			System.out.println("Bye");
			b++;
		}

	}

}
