package com.class1;

public class DataTypes {

	public static void main(String[] args) {
		
		//creating variables : type, name, value
		//number family
		byte b=-128;
		short s=3276;
		int i=678769;
		long l=7587558578667l;
		
		//number with decimals
		float f=2.2f;
		double d=3.7685665465;
		
		//character
		char c='a';
		
		//boolean (true or false)
		boolean g=false;
		
		//string
		String str="A123%$%#";
		
		System.out.println(b);
		
   }

}
